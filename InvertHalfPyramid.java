package Pattern;
/*
  Invert Half Pyramid
    * * * * *
    * * * *
    * * *
    * *
    *
 */
public class InvertHalfPyramid {
    public static void main(String[] args) {
    int row=0;
    for( int i=0;i<row;i++){
        for(int j=i;j<row;j++){
            System.out.print("* ");
        }
        System.out.println();
    }
}
}
